    private void checkBoxNuevoCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBoxNuevoCliActionPerformed
        boolean acc = checkBoxNuevoCli.isSelected();

        if (acc) {
            comboClientes.setEnabled(false);
            txtNombreCliente.setEnabled(true);
            campoNombreCliente.setEnabled(true);
            desactivarCampos(panelCampos, false);
            reestableceCamposVenta();
        } else {
            campoNombreCliente.setText("");
            campoNombreCliente.setEnabled(false);
            txtNombreCliente.setEnabled(false);
            comboClientes.setEnabled(true);
            desactivarCampos(panelCampos, true);
            reestableceCamposVenta();
            checkBoxImprimirNota.setEnabled(false);
            btnRealizarVenta.setEnabled(false);
        }

    }//GEN-LAST:event_checkBoxNuevoCliActionPerformed

