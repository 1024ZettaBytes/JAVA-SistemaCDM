private void establecerClientes() {
        DefaultComboBoxModel modeloLista;
        Object[] arregloClientes;
        // Si hay clientes en la lista
        if (listaClientes.size() > 0) {
            // Se guardan en el arreglo  
            arregloClientes = listaClientes.toArray();
            modeloLista = new DefaultComboBoxModel(arregloClientes);
            comboClientes.setModel(modeloLista);
            comboClientes.setEnabled(true);
            checkBoxNuevoCli.setEnabled(true);
        } else {
            // Se guarda un solo valor indicando el resultado de la búsqueda
            arregloClientes = new Object[1];
            arregloClientes[0] = "-------SIN CLIENTES DISPONIBLES------";
            modeloLista = new DefaultComboBoxModel(arregloClientes);
            comboClientes.setModel(modeloLista);

            // Se desactivan todos los componentes inferiores
            desactivarCampos(panelCampos, false);
            checkBoxNuevoCli.setEnabled(false);
            checkBoxNuevoCli.setSelected(true);
        }

    }
