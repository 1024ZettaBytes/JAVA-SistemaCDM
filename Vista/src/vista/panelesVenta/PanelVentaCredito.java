    private void btnRealizarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarVentaActionPerformed
        ConfirmacionVenta conf = new ConfirmacionVenta(parent, true, total);
        conf.setVisible(true);
        if (conf.aceptado) {
            Cliente cliente = null;
            int de = (Integer) txtCantDesayuno.getValue();
            int co = (Integer) txtCantComida.getValue();
            int ce = (Integer) txtCantCena.getValue();
            Venta v = new Venta(0, new Timestamp(Calendar.getInstance().getTimeInMillis()), Control.usuarioActivo);
            // Si es modificacion de un cliente
            if (!checkBoxNuevoCli.isSelected()) {
                cliente = (Cliente) comboClientes.getSelectedItem();
                cliente.setCreditoDesayuno(cliente.getCreditoDesayuno() + de);
                cliente.setCreditoComida(cliente.getCreditoComida() + co);
                cliente.setCreditoCena(cliente.getCreditoCena() + ce);
                // Si se genera correctamente la venta
                if (Control.ventas.agregar(v)) {
                    v = Control.ventas.consultarUltimaVenta();
                    // Se genera la venta de credito
                    VentaCredito vc = new VentaCredito(0, v, cliente, de, co, ce, total);
                    Control.ventasCredito.agregarVentaCredito(vc);
                    Control.clientes.actualizar(cliente);
                     JOptionPane.showMessageDialog(parent, "Venta realizada", "Exito", JOptionPane.INFORMATION_MESSAGE);
                     listaClientes = Control.clientes.consultarListaOrdenada();
                     establecerClientes();
                     reestableceCamposVenta();
                } else {
                    JOptionPane.showMessageDialog(parent, "No se pudo generar la venta", "Imposible", JOptionPane.ERROR_MESSAGE);
                }
            }// Si es cliente nuevo
            else{
                cliente= new Cliente(0,campoNombreCliente.getText(), de, co, ce);
                if (Control.ventas.agregar(v)) {
                    v = Control.ventas.consultarUltimaVenta();
                    Control.clientes.agregar(cliente);
                    cliente = Control.clientes.consultarLista().get(listaClientes.size());
                    // Se genera la venta de credito
                    VentaCredito vc = new VentaCredito(0, v, cliente, de, co, ce, total);
                    Control.ventasCredito.agregarVentaCredito(vc); 
                     JOptionPane.showMessageDialog(parent, "Venta realizada", "Exito", JOptionPane.INFORMATION_MESSAGE);
                     listaClientes = Control.clientes.consultarListaOrdenada();
                     establecerClientes();
                     reestableceCamposVenta();
                } else {
                    JOptionPane.showMessageDialog(parent, "No se pudo generar la venta", "Imposible", JOptionPane.ERROR_MESSAGE);
                }
            }
        }

    }//GEN-LAST:event_btnRealizarVentaActionPerformed

